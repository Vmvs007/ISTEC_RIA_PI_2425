nota1 = int(input("Insira a 1º Nota: "))
nota2 = int(input("Insira a 2º Nota: "))
nota3 = int(input("Insira a 3º Nota: "))

mediaAritmetica = (nota1 + nota2 + nota3) / 3

print("Média Aritmética:", mediaAritmetica)

mediaPonderada = (nota1 * 0.2) + (nota2 * 0.3) + (nota3 * 0.5)
print("Média Ponderada:", mediaPonderada)
