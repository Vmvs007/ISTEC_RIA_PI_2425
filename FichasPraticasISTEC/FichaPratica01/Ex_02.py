numero1 = int(input("Insira um número: "))
numero2 = int(input("Insira outro número: "))

# SOMA
resultado=numero1+numero2
print("Soma:",resultado)

# SUBTRAÇÃO
resultado=numero1-numero2
print("Subtração:",resultado)

# MULTIPLICAÇÃO
resultado=numero1*numero2
print("Multiplicação:",resultado)

# DIVISÃO
resultado=numero1/numero2
print("Divisão:",resultado)
